package artist.dto;

public class Artist_NewsDTO {
    private String exhibitions_title;
    private String exhibitions_term;
    private String news_date;
    private String news_title;
    private String news_img;
    private String news_href;

    public String getExhibitions_title() {
        return exhibitions_title;
    }

    public void setExhibitions_title(String exhibitions_title) {
        this.exhibitions_title = exhibitions_title;
    }

    public String getExhibitions_term() {
        return exhibitions_term;
    }

    public void setExhibitions_term(String exhibitions_term) {
        this.exhibitions_term = exhibitions_term;
    }

    public String getNews_date() {
        return news_date;
    }

    public void setNews_date(String news_date) {
        this.news_date = news_date;
    }

    public String getNews_title() {
        return news_title;
    }

    public void setNews_title(String news_title) {
        this.news_title = news_title;
    }

    public String getNews_img() {
        return news_img;
    }

    public void setNews_img(String news_img) {
        this.news_img = news_img;
    }

    public String getNews_href() {
        return news_href;
    }

    public void setNews_href(String news_href) {
        this.news_href = news_href;
    }
}
