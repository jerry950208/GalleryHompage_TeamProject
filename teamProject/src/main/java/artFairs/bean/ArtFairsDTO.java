package artFairs.bean;

public class ArtFairsDTO {
	private String title;
	private String afdate;
	private String mainimg_src;
	private String info_date;
	private String info;
	private String detailimg_src;
	private String place;
	private String place_detail;
	private String artists;
	private String artist_name;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAfdate() {
		return afdate;
	}
	public void setAfdate(String afdate) {
		this.afdate = afdate;
	}
	public String getMainimg_src() {
		return mainimg_src;
	}
	public void setMainimg_src(String mainimg_src) {
		this.mainimg_src = mainimg_src;
	}
	public String getInfo_date() {
		return info_date;
	}
	public void setInfo_date(String info_date) {
		this.info_date = info_date;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public String getDetailimg_src() {
		return detailimg_src;
	}
	public void setDetailimg_src(String detailimg_src) {
		this.detailimg_src = detailimg_src;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public String getPlace_detail() {
		return place_detail;
	}
	public void setPlace_detail(String place_detail) {
		this.place_detail = place_detail;
	}
	public String getArtists() {
		return artists;
	}
	public void setArtists(String artists) {
		this.artists = artists;
	}
	public String getArtist_name() {
		return artist_name;
	}
	public void setArtist_name(String artist_name) {
		this.artist_name = artist_name;
	}
}
